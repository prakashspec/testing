Main.forest.prototype.updateCounter50 =function(t){


            this.eagle.x = this['path'+t][this['pi'+t]].x;
            this.eagle.y = this['path'+t][this['pi'+t]].y;
			this.eagle.rotation = this['path'+t][this['pi'+t]].angle+360;
			if(Math.round(this.path50[this.pi50].x)==this.points50.x[1] && this.flag)
			{
			this.flag = false;
			game.add.tween(this.eagle.scale).to({ x: 0.7, y: 0.7 }, 500, Phaser.Easing.Linear.None, true)
			}
            this['pi'+t]++;



             if (this['pi'+t] >= this['path'+t].length)
            {
			
            game.time.events.remove(this.Etween);
			this['pi'+t]=0;
			this.eagle.scale.setTo(1);
			this.removeRat(); 
            }
}
Main.forest.prototype.eaglePlot =function(t){
			this.path50 = [];
            var ix = 0;
            var x = Main.browser / game.width;
			
            for (var i = 0; i <= 1; i += x)
            {
                var px = this.math.bezierInterpolation(this.points50.x, i);
                var py = this.math.bezierInterpolation(this.points50.y, i);
                var node = { x: px, y: py, angle: 0 };
                if (ix > 0)
                {
                    node.angle = this.math.angleBetweenPoints(this.path50[ix - 1], node);
                }
                this.path50.push(node);
                ix++;
            }

            

}
